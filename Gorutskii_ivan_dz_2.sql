-- mysqldump - служит для переноса базы данных с одного сервера на другой (создание резервной копии)

-- Создадим новую БД sample
CREATE DATABASE sample;

-- Открываем консоль
-- создадим dump базы данных example
mysqldump example > example.sql

-- Разверем дамп в БД sample
