-- Проверяем если БД сущестует удаляем
DROP DATABASE IF EXISTS example;
-- Создаем БД
CREATE DATABASE example;
-- Выбираем нашу БД
USE example;

-- Проверяем сущестует таблица если да, то удаляем ее чтобы следующей командой создать
-- это нужно для того чтобы если вдруг нам придется пересоздать один кусок скрипта а не всю БД
DROP TABLE IF EXISTS users;
CREATE TABLE users(
	id SERIAL PRIMARY KEY,
	name VARCHAR(150) COMMENT 'Имя пользователя'
) COMMENT = 'Пользователи';

